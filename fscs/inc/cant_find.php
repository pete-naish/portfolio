<h2>Can't find your bank or building society?</h2>
<p>The FSCS protects savings up to &pound;85,000 with all banks, building societies and credit unions that are authorised by the PRA and the Financial Conduct Authority (FCA). This applies to individuals and small businesses.</p>
<p>If you can't find your bank, building society or credit union on the checker, your savings might not be protected by the FSCS. You can check if your provider is authorised by:</p>
<ul>
	<li>Searching the <a href="http://www.fsa.gov.uk/register/firmSearchForm.do" target="_blank">Financial Services Register</a>, which contains details of all financial services providers which are authorised by the PRA and FCA.</li>
	<li>Contacting your bank, building society or credit union and asking for their authorisation number.</li>
	<li>Checking the website of your bank, building society or credit union - the authorisation number is often in the 'legal information' section.</li>
</ul>
<p>If your financial service provider is not authorised by the PRA and FCA your savings will not be protected by the FSCS.</p>
