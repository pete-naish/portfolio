				<h3>Your savings are FSCS protected if:</h3>
				<ul class="first">
					<li>You have less than &pound;85,000 of savings</li>
					<li>Your savings are with banks, building societies or credit unions which are authorised by the Prudential Regulation Authority (PRA)</li>

				</ul>
				<h3>Your savings might not be FSCS protected if:</h3>
				<ul>
					<li>You have savings with banks, building societies or credit unions which are not authorised by the PRA</li>
					<li>You have more than &pound;85,000 in an account or more than &pound;170,000 in a joint account</li>
					<li>You have more than &pound;85,000 (or &pound;170,000 in joint accounts) split across banks, building societies and credit unions which share a banking authorisation</li>
				</ul>
				<p>To find out if your banks, building societies and credit unions share a banking authorisation use the <a href="#fifth" class="footerLink">FSCS Protection Checker</a></p>
